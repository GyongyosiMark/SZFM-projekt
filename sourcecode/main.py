class Player:
    def __init__(self, x, y):
        #player animacio, pozicio, mozgas
        img = pygame.image.load('../animation/p1_stand.png')
        self.image = pygame.transform.scale(img, (40, 80))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.width = self.image.get_width()
        self.height = self.image.get_height()
        self.vel_y = 0
        self.jumped = False

    def update(self):
        dx = 0
        dy = 0

        # input a billentyukrol
        key = pygame.key.get_pressed()
        if key[pygame.K_SPACE] and self.jumped == False:
            self.vel_y = -15
            self.jumped = True
        if not key[pygame.K_SPACE]:
            self.jumped = False
        if key[pygame.K_LEFT]:
            dx -= 5
        if key[pygame.K_RIGHT]:
            dx += 5

        # gravitacio
        self.vel_y += 1
        if self.vel_y > 10:
            self.vel_y = 10
        dy += self.vel_y

        #utkozes TO DO
        for tile in world.tile_list:
            # vizsszintes utkozes
            if tile[1].colliderect(self.rect.x + dx, self.rect.y, self.width, self.height):
                dx = 0
            # fuggoleges utkozes
            if tile[1].colliderect(self.rect.x, self.rect.y + dy, self.width, self.height):
                # alulrol utkozes blokkal, ugrasnal
                if self.vel_y < 0:
                    dy = tile[1].bottom - self.rect.top
                    self.vel_y = 0
                # felulrol utkozes blokkal, esesnel
                elif self.vel_y >= 0:
                    dy = tile[1].top - self.rect.bottom
                    self.vel_y = 0

        # player poziziojanak frissittese
        self.rect.x += dx
        self.rect.y += dy

        if self.rect.bottom > screen_height:
            self.rect.bottom = screen_height
            dy = 0

        # player rajzolasa a kepenyore
        screen.blit(self.image, self.rect)

